#ifndef GAMEMATH_H
#define GAMEMATH_H
//------------------------------------------------------------------------
#include "Types.h"
//------------------------------------------------------------------------

//------------------------------------------------------------------------
// GameMath namespace is used to move or zoom 
// a single or a group of coordinates in 2d plane
//------------------------------------------------------------------------
namespace GameMath
{
	//------------------------------------------------------------------------
	// 2d displacement of a set of coordinates
	//------------------------------------------------------------------------
	void Translation(Vec2[], size_t, Vec2);

	//------------------------------------------------------------------------
	// Scale a set of coordinates
	//------------------------------------------------------------------------
	void Scaling(Vec2[], size_t, float);

	//------------------------------------------------------------------------
	// Calculate the distance between two coordinates
	//------------------------------------------------------------------------
	float Distance(Vec2, Vec2);

	//------------------------------------------------------------------------
	// 2d displacement of a single coordinates
	//------------------------------------------------------------------------
	void TranslatePoint(Vec2&, Vec2);

	//------------------------------------------------------------------------
	// Scale a single coordinate
	//------------------------------------------------------------------------
	void ScalePoint(Vec2&, float);
}

#endif // !GAMEMATH_H
